<h1>1. Tijeras</h1> 
  <p>Para este ejercicio has de tumbarte boca arriba. Luego levanta una de tus piernas, siempre manteniéndola recta. Cógela mientras elevas tu tronco hacia arriba y acércala más a ti. Cuando bajes una pierna sube la otra, pero procura que ninguna de las dos toque el suelo en ningún momento.</p>
<br>


<h1>2. Ejercicio para aumentar la flexibilidad de las rodillas</h1> 
  <p>Lo primero es buscar una silla o cualquier otro objeto que tengas a mano y pueda ayudarte a mantener el equilibrio. Luego dobla una de tus piernas hacia atrás y coge el pie con una de tus manos, manteniéndolo más o menos a la altura de tus glúteos. Tira de él hasta que notes que el talón roza el glúteo, aquí tendrías que estar notando como se estira el cuádriceps. Cuando acabes, pasa a estirar la otra pierna.</p>
  <p>Este estiramiento, como has visto, te ayuda a aumentar la flexibilidad tanto en los cuádriceps como en las rodillas.</p>
<br>

<h1>3. Estiramiento de valla</h1>
<img class="img-fluid" src="<?php echo base_url()?>imagenes/35.jpg" alt=""width="500" height="500" />   
  <p>Se le llama así porque puedes hacerlo de pie y apoyándote en una valla. No obstante, se trata de un ejercicio que también puedes hacer sentado en el suelo, usando la esterilla habitual. Elige la opción que más cómoda te resulte y pasa a hacer estos movimientos.</p>
  <p>Deja una pierna atrás y estira la otra hacia delante, sin flexionar la rodilla. El siguiente paso es echar tu cuerpo hacia delante, desde las caderas. El objetivo es que tu abdomen esté lo más cerca posible de la rodilla. También deberías llegar a coger el pie con las manos, estirándolo hacia ti. Sigue con la otra pierna cuando acabes.</p>
  <p>Si eres principiante, es fácil que tiendas a doblar la rodilla. Procura evitarlo y llega hasta donde puedas. Seguro que cada día podrás ir un poco más lejos.</p>
<br>

<h1>4. Estiramiento para cuádriceps y gemelos</h1>
  <p>Este es un ejercicio bastante completo que te ayuda a aumentar la flexibilidad de las piernas. Además, no es demasiado difícil, por lo que es perfecto si estás empezando.</p>
  <p>Para hacerlo necesitas estar apoyado contra un muro o pared. De pie, adelanta una de las piernas y forma un ángulo de 90 grados mientras mantienes la otra pierna completamente estirada. Entonces tendrás que apoyar las manos sobre la pared y llevar la piernas que tienes atrasada cada vez más atrás, hasta que notes la presión en los gemelos. Todo lo que tienes que hacer es mantener la espalda recta, el ángulo de 90 grados con una pierna, y estirar la otra cada vez más sin despegar el pie del suelo. Como decíamos en el ejercicio anterior, no te preocupes si no puedes forzar mucho en un principio. Solo tienes que ser constante para ir notando la evolución.</p>
<br>

<h1>5. Estiramiento para isquiotibiales</h1>
<img class="img-fluid" src="<?php echo base_url()?>imagenes/36.jpg" alt=""width="500" height="500" />   
  <p>Vamos a terminar tu rutina de entrenamiento con un ejercicio para los isquiotibiales. Así aumentarás la elasticidad y conseguirás eliminar esa sensación de rigidez tan molesta.</p>
  <p>Tienes que colocarte de pie y poner las piernas rectas. Luego bajas, siempre manteniendo la espalda recta, sin flexionar las rodillas. El objetivo es tocar los pies con las manos, pero llega hasta donde puedas e intenta ir un poco más lejos cada día. Haz este movimiento primero hacia delante y luego hacia los lados.</p>
  <p>Estos han sido algunos ejercicios de flexibilidad de piernas que puedes hacer para mejorar la elasticidad. Hoy en día hay muchos trabajos sedentarios que no son lo mejor para nuestro estado físico. La rigidez se acaba apoderando de nosotros al final del día después de pasar ocho horas sentados. Por eso es importante hacer lo posible para aumentar la flexibilidad. Además, ten siempre presente que ser flexible ayuda a padecer menos lesiones.</p>
  <p>Como ves, los ejercicios de flexibilidad de piernas que te hemos propuesto no son muy difíciles. Solo has de tener paciencia y esforzarte para lograr un mejor resultado cada día. Estamos seguros de que lo conseguirás. ¡No olvides descargar tu infografía!</p>
