<h1>¿Cómo adelgazar piernas y muslos?</h1> 
<p>A menudo, lo más complicado no es perder peso, sino reducir la grasa acumulada en la parte del cuerpo que quieras. Además, si tu objetivo es eliminar esos kilos de más de los muslos, la misión se complica.</p>
<img class="img-fluid" src="<?php echo base_url()?>imagenes/41.jpg" alt=""width="900" height="500" />   
  <p>Se dice que el muslo es la parte del cuerpo que más cuesta adelgazar. Para lograr el propósito marcado, lo primero que tendrás que hacer es armarte de paciencia, ya que, aunque existen maneras sanas de acortar los tiempos del proceso, la experiencia dice que la cosa no es tan rápida como algunos pronostican alegremente.</p>
<br>

<h1>Ejercicios para adelgazar muslos</h1> 
<br>
<h1>1. Sentadillas de sumo</h1>
<img class="img-fluid" src="<?php echo base_url()?>imagenes/42.jpg" alt=""width="900" height="500" />   
  <p>Empieza por hacer series de 20 sentadillas de sumo para adelgazar los muslos. Abre las piernas con las manos en la cintura y flexiona ligeramente las rodillas situando el interior del pie en paralelo a los muslos. La sentadilla de sumo también es una técnica muy buena para los glúteos.</p>
<br>

<h1>2. Elevación de pierna lateral</h1>
  <p>Haz 40 series levantando la pierna de lado. Comienza poniendo las piernas juntas y las manos en la cintura y, manteniendo las manos en la cintura, levanta la pierna hacia el lado estirándola firmemente.</p>
<br>

<h1>3. Zancada lateral con deslizamiento</h1>  
<img class="img-fluid" src="<?php echo base_url()?>imagenes/43.jpg" alt=""width="900" height="500" />  
  <p>Practica también veinte series de este movimiento para fortalecer la zona de los muslos. Coloca las manos en la cintura y abre ligeramente las piernas. Estira una pierna, mientras flexionas ligeramente la rodilla de la otra pierna. Todo esto manteniendo las manos en la cintura./p>
<br>

<h1>3. Extensiones pierna lateral</h1> 
<img class="img-fluid" src="<?php echo base_url()?>imagenes/44.jpg" alt=""width="900" height="500" />  
  <p>Otro buen ejercicio para adelgazar los muslos es el que te presentamos en este punto. Estírate en el suelo y baja la cabeza hacia el suelo y coloca justo debajo los codos ligeramente flexionados. Con las piernas dobladas suavemente, levanta cada una de las piernas y mantenlas estiradas. Haz 20 series de este ejercicio.</p>
<br>

<h1>5. Zancada lateral con deslizamiento</h1>  
  <p>Es un movimiento similar al anterior. Túmbate en el suelo y aguanta el cuerpo con las manos estiradas y las piernas formando un ángulo recto. Levanta cada una de las piernas de lado veinte veces.</p>
<br>

<h1>6. Elevación pierna lateral</h1> 
<img class="img-fluid" src="<?php echo base_url()?>imagenes/45.jpg" alt=""width="900" height="500" />  
  <p>Para adelgazar los muslos puedes probar también haciendo este ejercicio. Túmbate de lado, colocando la mano por detrás de la cabeza. Con la pierna bien estirada, levanta la otra cuarenta veces. Repite el movimiento con ambas piernas.</p>
