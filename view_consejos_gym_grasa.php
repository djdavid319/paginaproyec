<h1>Guía de ejercicios de lumbares</h1> 

<h1>Ejercicio 1: cardio</h1> 
<img class="img-fluid" src="<?php echo base_url()?>imagenes/37.jpg" alt=""width="500" height="500" />   
  <p>Realizar ejercicio cardiovascular para quemar el exceso de grasa es un fantástico ejercicio pese no estar trabajando las lumbares. Céntrate en los ejercicios que requieren el movimiento de todo el cuerpo, como correr, nadar, caminar a paso ligero, andar en bicicleta o hacer remo. El Colegio Americano de Medicina Deportiva recomienda que los adultos con sobrepeso deben aumentar su actividad a 45 minutos de ejercicio al día para aumentar la pérdida de peso y evitar recuperarlo al cabo de cierto tiempo.</p>
<br>

<h1>Ejercicio 2: superman</h1>
<img class="img-fluid" src="<?php echo base_url()?>imagenes/38.jpg" alt=""width="500" height="500" />   
  <p>Realiza supermans. Este es un ejercicio de lumbares excelente en el cual estiraremos y fortaleceremos las lumbares. Acuéstate boca abajo en el suelo, con las piernas juntas y totalmente extendidas y los brazos extendidos por encima de la cabeza. Estabiliza los músculos abdominales, contrae la espalda baja, y al mismo tiempo levanta los brazos y las piernas del suelo. Mantén la posición durante unos cinco segundos, y luego relaja.</p>
<br>

<h1>Ejercicio 3: peso muerto</h1>
<img class="img-fluid" src="<?php echo base_url()?>imagenes/39.jpg" alt=""width="500" height="500" /> 
  <p>Haz repeticiones de peso muerto con barra y piernas estiradas. Coloca una barra cargada en el suelo, y de pie con las piernas ligeramente separadas y las espinillas contra la barra. Mantén las piernas lo más recto posible, dobla la cintura hacia adelante con el abdomen apretado, pecho hacia delante y espalda arqueada. Sujeta la barra con un agarre en pronación. Mantén los brazos relajados, levanta la barra girando las caderas de pie con la espalda recta. Inclínate hacia delante para volver a la posición inicial.</p>
<br>

<h1>Ejercicio 4: lumbares en silla romana</h1>  
<img class="img-fluid" src="<?php echo base_url()?>imagenes/40.jpg" alt=""width="500" height="500" />  
  <p>Realiza lumbares. Este es, sin duda, uno de los ejercicios de lumbares más efectivos. Colócate boca abajo en una silla romana, con los tobillos debajo de los cojines de rodillos y el hueso púbico de la plataforma de apoyo. Junta las manos delante de la cabeza o en el pecho y comienza con la cabeza mirando hacia el suelo. Levanta el torso hasta llegar a ponerte en una postura completamente horizontal. Mantén la postura unos segundo y luego baja lentamente a la posición inicial. Para aumentar la resistencia, sostén un disco de peso apoyado contra el pecho mientras haces el ejercicio.</p>
  <p>Realiza 3 series de 8 a 12 repeticiones de cada uno de los ejercicios que hemos mencionado. Puedes hacer este circuito de ejercicios hasta 3 veces por semana, combinándolos con ejercicios para trabajar el resto de zonas musculares del cuerpo.</p>
<br>

<h1>Ejercicio 5: buenos días</h1> 
  <p>Realiza buenos días. Ponte de pie con los pies ligeramente separados, y coloca una barra sobre la espalda y hombros, sujetándola con las manos. Endurece el torso y contrae los músculos abdominales. Manteniendo la espalda recta, flexiona el torso hacia adelante hasta que sientas un estiramiento en los isquiotibiales. Levanta el torso para volver a la posición inicial.</p>
  <p>Ahora que ya conoces los mejores ejercicios de lumbares para quemar grasa ya puedes ponerlos en práctica. Si eres constante, notarás los resultados enseguida. Desde Mundo Fitness os retamos a comentar los ejercicios de lumbares que practicáis vosotros. </p>
