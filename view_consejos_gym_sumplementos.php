<h1>PROTEÍNA</h1>
  <p>Además de formar músculo, la proteína quema la grasa. Las dietas con un índice elevado de proteínas y bajo de hidratos de carbono son muy beneficiosas para adelgazar, ya que provocan una pérdida máxima de la grasa corporal y mínima de la masa muscular.</p>

<br>

<h1>PROTEÍNA DE SUERO DE LECHE</h1>
  <p>Se trata de un tipo de proteína singular, que mejora el rendimiento atlético y la salud bajo condiciones clínicas controladas. La proteína de suero de leche disminuye el apetito y el hambre. Además incluye proteína whey que mejora tu metabolismo y reduce el déficit calórico.</p>

