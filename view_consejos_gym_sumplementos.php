<h1>CLA</h1>
  <p>El CLA se obtiene del aceite de cártamo. Se trata de un ácido graso esencial de la familia de los omega 6 y uno de los nutrientes para reducir la grasa corporal sobre los que más se ha investigado. El CLA estimula la degradación y posterior digestión de las grasas, ya que actúa sobre las células grasas y las células musculares, el principal lugar donde la grasa se quema para obtener energía. El objetivo número 1 del CLA es conseguir buenos resultados en la tonificación corporal, ya que no reduce el peso a expensas de perder agua o masa muscular, sino únicamente grasas. Otra ventaja es que provoca estos efectos por sí solo, sin necesidad de realizar ejercicio ni de seguir dietas muy estrictas. Es excelente para reforzar el sistema inmunitario, posee acción antioxidante y antiinflamatoria y reduce los niveles de colesterol. Como todo en esta vida hay que tener en cuenta las dosis diarias, ya que puede ser tanto positivo como negativo.</p>

<br>

<h1>L-CARNITINA</h1>
  <p>La L-carnitina ocupa un lugar destacado en esta lista, es una molécula que  fabrica tu propio cuerpo partir del aminoácido lisina y la vitamina C. Es una sustancia que ejerce un papel muy importante en el metabolismo de las grasas. Se encarga de transportar los ácidos grasos de cadena larga hacia la mitocondria, la fuente de energía celular, y así ayuda en la combustión de las grasas. Además, la L-carnitina aumenta el rendimiento deportivo, ya que utiliza las grasas como fuente de energía.</p>

