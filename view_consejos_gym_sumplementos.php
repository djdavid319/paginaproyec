  <h1>TÉ VERDE</h1>
  <p>El té verde se ha vuelto muy popular debido a sus capacidades antioxidantes, entre otras. También hay que destacar que son muy buena opción para la pérdida de peso ya que contiene unos niveles elevados de cafeína y de polifenoles de catequina, que pueden repartir el gasto de energía diario y el uso de las grasas. A través de la termogénesis y/o de la oxidación de las grasas, el extracto de té verde ejerce un papel importante en el control de la composición corporal.</p>

<br>

  <h1>TERMOGÉNICOS</h1>
  <p>La termogénesis consiste en la producción de calor por parte del organismo debido a un aumento del metabolismo (el conjunto de transformaciones que las sustancias químicas sufren en el interior de los organismos vivos). Cualquier proceso metabólico necesita energía, que se obtiene a partir de los alimentos en forma de calorías (la unidad de medida del calor). Los termogénicos aumentan el metabolismo. La fuente de energía que se utiliza para ello proviene casi exclusivamente de la degradación de las grasas acumuladas en el cuerpo. Todo este proceso hace que se gaste energía, quemándose, por lo tanto, las grasas. Además, este tipo de suplementos hacen que nuestro cuerpo queme la grasa sin necesidad de recurrir al deporte, pero claro que si realizamos ejercicio físico, potenciamos sus efectos.</p>

